#include <stdio.h>
/**
 * main - Prints the first 50 Fibonacci numbers
 * Return: 0
 */
int main(void)
{
unsigned long a = 0;
unsigned long b = 1;
int c;
unsigned long add;
for (c = 0; c < 98; c++)
{
add = a + b;
printf("%llu", add);
a = b;
b = add;
if (c < 98)
{
printf(", ");
}
}
putchar('\n');
return (0);
}
